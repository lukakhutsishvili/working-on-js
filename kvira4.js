// рЃќрЃЮрЃњрЃўрЃћрЃарЃЌрЃў рЃљрЃЊрЃљрЃЏрЃўрЃљрЃюрЃў рЃџрЃћрЃЦрЃфрЃўрЃћрЃЉрЃА рЃАрЃљрЃЎрЃЏрЃљрЃЮрЃЊ рЃАрЃгрЃарЃљрЃцрЃљрЃЊ рЃЎрЃўрЃЌрЃ«рЃБрЃџрЃЮрЃЉрЃА; рЃЏрЃљрЃЌрЃў рЃерЃћрЃюрЃћрЃџрЃћрЃЉрЃљ рЃљрЃю рЃЌрЃБрЃюрЃЊрЃљрЃф, рЃАрЃўрЃбрЃДрЃЋрЃћрЃЉрЃА рЃерЃЮрЃарЃўрЃА рЃърЃљрЃБрЃќрЃўрЃА рЃњрЃљрЃЎрЃћрЃЌрЃћрЃЉрЃљ рЃЎрЃљрЃарЃњрЃў рЃўрЃЦрЃюрЃћрЃЉрЃЮрЃЊрЃљ... рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљ, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃгрЃўрЃюрЃљрЃЊрЃљрЃЊрЃћрЃЉрЃўрЃА рЃЕрЃљрЃгрЃћрЃарЃљрЃА рЃЏрЃЮрЃњрЃЌрЃ«рЃЮрЃЋрЃЌ, рЃерЃћрЃЏрЃЊрЃћрЃњ рЃЎрЃў рЃўрЃЏрЃљрЃЋрЃћ рЃЕрЃљрЃюрЃљрЃгрЃћрЃарЃА рЃњрЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃЌ, рЃЮрЃдрЃЮрЃюрЃЊ рЃўрЃЏ рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃўрЃЌ, рЃарЃЮрЃЏ рЃљрЃЏрЃ»рЃћрЃарЃљрЃЊ рЃљрЃАрЃЮрЃћрЃЉрЃА рЃерЃЮрЃарЃўрЃА рЃДрЃЋрЃћрЃџрЃљ рЃАрЃўрЃЋрЃарЃфрЃћ (whitespace) рЃЏрЃарЃљрЃЋрЃљрЃџрЃгрЃћрЃарЃбрЃўрЃџрЃўрЃЌ (...) рЃўрЃЦрЃюрЃћрЃЉрЃљ рЃЕрЃљрЃюрЃљрЃфрЃЋрЃџрЃћрЃЉрЃБрЃџрЃў.

// рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃљрЃЊ:

// > This is Bitcamp.
// > This...is...Bitcamp.

const playback = () => {
    let str = prompt("write some: ");
    let newStr = "";
    for (let i = 0; i < str.length; i++) {
        if (str[i] == " ") {
            newStr += "...";
        } else {
            newStr += str[i];
        }
    }
    console.log(newStr);
}
playback();

const playbacks = () => {
  let str = prompt("write some: ");
  str = str.split(" ").join("...");
  console.log(str);
};
playbacks();

