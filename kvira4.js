// emoji-рЃЏрЃЊрЃћ рЃљрЃарЃАрЃћрЃЉрЃЮрЃЉрЃЊрЃљ emoticon-рЃћрЃЉрЃў, рЃАрЃљрЃЊрЃљрЃф рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃўрЃА рЃћрЃарЃЌрЃЮрЃЉрЃџрЃўрЃЮрЃЉрЃљ - ":)" рЃњрЃљрЃдрЃўрЃЏрЃћрЃЉрЃБрЃџ рЃАрЃљрЃ«рЃћрЃА рЃљрЃдрЃюрЃўрЃерЃюрЃљрЃЋрЃЊрЃљ, рЃ«рЃЮрЃџрЃЮ ":(" - рЃЏрЃЮрЃгрЃДрЃћрЃюрЃўрЃџрЃА. рЃЊрЃдрЃћрЃА рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃћрЃЉрЃерЃў рЃћрЃА рЃАрЃўрЃЏрЃЉрЃЮрЃџрЃЮрЃћрЃЉрЃў emoji-рЃЊ рЃљрЃЋрЃбрЃЮрЃЏрЃљрЃбрЃБрЃарЃљрЃЊ рЃњрЃљрЃарЃЊрЃљрЃўрЃЦрЃЏрЃюрЃћрЃЉрЃљ.

// рЃерЃћрЃЦрЃЏрЃћрЃюрЃўрЃЌ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃерЃў, рЃАрЃљрЃЊрЃљрЃф рЃЊрЃљрЃгрЃћрЃарЃЌ рЃцрЃБрЃюрЃЦрЃфрЃўрЃљрЃА рЃАрЃљрЃ«рЃћрЃџрЃгрЃЮрЃЊрЃћрЃЉрЃўрЃЌ convert, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃърЃљрЃарЃљрЃЏрЃћрЃбрЃарЃљрЃЊ рЃўрЃдрЃћрЃЉрЃА рЃАрЃбрЃарЃўрЃЦрЃЮрЃюрЃА (str), рЃерЃћрЃЏрЃЊрЃћрЃњ рЃЎрЃў рЃўрЃЏрЃљрЃЋрЃћ рЃАрЃбрЃарЃўрЃЦрЃЮрЃюрЃА рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА, рЃўрЃЏ рЃњрЃљрЃюрЃАрЃ«рЃЋрЃљрЃЋрЃћрЃЉрЃўрЃЌ, рЃарЃЮрЃЏ рЃюрЃћрЃЉрЃўрЃАрЃЏрЃўрЃћрЃа рЃљрЃЏрЃњрЃЋрЃљрЃа рЃЕрЃљрЃюрЃљрЃгрЃћрЃарЃА - ":)" ­ЪЎѓ-рЃўрЃЌ, рЃ«рЃЮрЃџрЃЮ ":(" - ­ЪЎЂ-рЃўрЃЌ рЃерЃћрЃфрЃЋрЃџрЃўрЃА. рЃЊрЃљрЃюрЃљрЃарЃЕрЃћрЃюрЃў рЃбрЃћрЃЦрЃАрЃбрЃў рЃБрЃфрЃЋрЃџрЃћрЃџрЃў рЃБрЃюрЃЊрЃљ рЃЊрЃљрЃарЃЕрЃћрЃА.

// рЃўрЃЏрЃљрЃЋрЃћ рЃърЃарЃЮрЃњрЃарЃљрЃЏрЃљрЃерЃў, рЃЊрЃљрЃгрЃћрЃарЃћрЃЌ рЃЏрЃћрЃЮрЃарЃћ рЃцрЃБрЃюрЃЦрЃфрЃўрЃљ - main, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃАрЃбрЃарЃўрЃЦрЃЮрЃюрЃўрЃА рЃерЃћрЃДрЃЋрЃљрЃюрЃљрЃА рЃњрЃЌрЃ«рЃЮрЃЋрЃЌ, рЃерЃћрЃЏрЃЊрЃћрЃњ convert рЃцрЃБрЃюрЃЦрЃфрЃўрЃљрЃА рЃўрЃФрЃљрЃ«рЃћрЃЉрЃА рЃЊрЃљ рЃЉрЃЮрЃџрЃЮрЃА, рЃерЃћрЃЊрЃћрЃњрЃА рЃњрЃљрЃЕрЃЋрЃћрЃюрЃћрЃЉрЃЌ.

// рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃљрЃЊ:

// > hello :)
// > hello ­ЪЎѓ
// > goodbye :(
// > goodbye ­ЪЎЂ

const convert = (str) =>{
    if(str.includes(":)")){
        return str.replace(":)",  "­ЪЎѓ")
    }else if(str.includes(":(")){
        return str.replace(":(", "­ЪЎѓ")
    }else {
        return str
    }
}
const main = () =>{
    const str = prompt("wrote string: ");
    return convert(str);
}

console.log(main())

